CREATE OR REPLACE PROCEDURE CORTEX.TESTING.CREATE_SALES_FACT()
RETURNS VARCHAR
LANGUAGE SQL
AS
BEGIN
    CREATE OR REPLACE TABLE CORTEX.TESTING.SALES_FACT AS
    SELECT
        d.CUSTOMER_ID,
        d.CUSTOMER_NAME,
        SUM(s.SALES) AS TOTAL_SALES
    FROM CORTEX.TESTING.CUSTOMER_DIM d
    INNER JOIN CORTEX.TESTING.CUSTOMER_SALES s
        ON d.CUSTOMER_ID = s.CUSTOMER_ID
    GROUP BY d.CUSTOMER_ID, d.CUSTOMER_NAME;
    RETURN 'SALES_FACT table created successfully';
END;

CALL CORTEX.TESTING.CREATE_SALES_FACT();

CREATE OR REPLACE TABLE CORTEX.TESTING.SALES_FACT_CSV AS
SELECT $1::INT AS CUSTOMER_ID, $2::VARCHAR AS CUSTOMER_NAME, $3::INT AS TOTAL_SALES
FROM @CORTEX.TESTING.CSV_STAGE/sales_fact.csv;

SELECT
    COALESCE(p.CUSTOMER_ID, c.CUSTOMER_ID) AS CUSTOMER_ID,
    p.CUSTOMER_NAME AS PROC_CUSTOMER_NAME,
    c.CUSTOMER_NAME AS CSV_CUSTOMER_NAME,
    p.TOTAL_SALES AS PROC_TOTAL_SALES,
    c.TOTAL_SALES AS CSV_TOTAL_SALES,
    CASE
        WHEN p.CUSTOMER_ID IS NULL THEN 'IN CSV ONLY'
        WHEN c.CUSTOMER_ID IS NULL THEN 'IN PROC ONLY'
        WHEN p.TOTAL_SALES != c.TOTAL_SALES THEN 'SALES MISMATCH'
        WHEN p.CUSTOMER_NAME != c.CUSTOMER_NAME THEN 'NAME MISMATCH'
        ELSE 'MATCH'
    END AS COMPARISON_STATUS
FROM CORTEX.TESTING.SALES_FACT p
FULL OUTER JOIN CORTEX.TESTING.SALES_FACT_CSV c
    ON p.CUSTOMER_ID = c.CUSTOMER_ID
ORDER BY COALESCE(p.CUSTOMER_ID, c.CUSTOMER_ID);




